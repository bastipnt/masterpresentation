<section id="web-tracking">
  <section id="web-tracking-use">
    <h3 class="text-left">Use:</h3>
    <ul class="w-full">
      <li>Personalization (recommendations, remembering preferences)</li>
      <li>Website performance (analytics, A/B testing)</li>
      <li>Security (bot detection, fraud prevention)</li>
      <li>Geolocation (regional content)</li>
    </ul>
  </section>

  <section id="web-tracking-misuse">
    <h3 class="text-left">Misuse:</h3>
    <ul class="w-full">
      <li>Uninformed user profiling</li>
      <li>Data sold to advertisers and brokers</li>
      <li>Manipulation (political, behavioral)</li>
      <li>Lack of user control or transparency</li>
      <li>High risk of data breaches</li>
    </ul>
  </section>

  <section id="web-tracking-methods">
    <h3 class="text-left">Methods:</h3>
    <ul class="w-full">
      <li>Cookie based tracking</li>
      <li>Tracking Pixels</li>
      <li>Fingerprinting</li>
    </ul>
  </section>
</section>
